<?php

use PHPUnit\Framework\TestCase;

final class ExampleBookSetUpTest extends TestCase
{
    /**
     * @var Book $book
     */
    private $book;

    protected function setUp()/* The :void return type declaration that should be here would cause a BC issue */
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->book = new Book();
    }

    public function testGetTitle(): void
    {
        static::assertSame('A Great Book', $this->book->getTitle());
    }

    public function testGetAuthor(): void
    {
        static::assertSame('John Doe', $this->book->getAuthor());
    }

    public function testTurnPage(): void
    {
        static::assertEmpty($this->book->turnPage());
    }

    public function testGetCurrentPage(): void
    {
        static::assertSame('current page content', $this->book->getCurrentPage());
    }
}